package org.nanocontainer.testmodel;


public class WebServerConfigComp implements WebServerConfig {

    private String host;
    private int port;

    public WebServerConfigComp(String host, int port) {
        this.host = host;
        this.port = port;
    }

    public String getHost() {
        return host;
    }

    public int getPort() {
        return port;
    }
}
